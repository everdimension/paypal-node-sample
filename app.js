require('dotenv').config();
const express = require('express');
const path = require('path');
const favicon = require('serve-favicon');
const logger = require('morgan');
const cookieParser = require('cookie-parser');
const bodyParser = require('body-parser');
const wpDevMiddleware = require('webpack-dev-middleware');
const webpack = require('webpack');
const wpConfig = require('./webpack.config');
const fileExists = require('./tools/fileExists');

if (!fileExists('./.env')) {
  console.error('You need .env file with paypal tokens to run the app');
  process.exit(1);
}

const routes = require('./routes/index');
const users = require('./routes/users');

const app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'jade');

app.use(favicon(path.join(__dirname, 'public', 'favicon.ico')));
app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: false }));
app.use(cookieParser());

app.use(express.static(path.join(__dirname, 'public')));
const wpCompiler = webpack(wpConfig);
app.use(wpDevMiddleware(wpCompiler, {
  noInfo: true,
  publicPath: wpConfig.output.publicPath,
  stats: { colors: true, error: true },
}))


app.use('/', routes);
app.use('/users', users);

require('./paypal')(app);

// catch 404 and forward to error handler
app.use(function(req, res, next) {
  const err = new Error('Not Found');
  err.status = 404;
  next(err);
});

// error handlers

// development error handler
// will print stacktrace
if (app.get('env') === 'development') {
  app.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.render('error', {
      message: err.message,
      error: err
    });
  });
}

// production error handler
// no stacktraces leaked to user
app.use(function(err, req, res, next) {
  res.status(err.status || 500);
  res.render('error', {
    message: err.message,
    error: {}
  });
});


module.exports = app;
